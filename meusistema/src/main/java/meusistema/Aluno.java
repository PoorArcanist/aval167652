package meusistema;

public class Aluno {
	private int id;
	private String nome;
	private int idade;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public static void main(String[] args) {
		AlunoDAO ad = new AlunoDAO();
		ad.getTodos();
	}

}
